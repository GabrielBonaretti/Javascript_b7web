let nome = "Bonaretti"                              // String
let idade = 90                                      // Number
let logado = true                                   // Boolean
let lista = ["ovo", "massa", "corante"]             // Array(Object)
let nomeCompleto = {nome: "Gabrile", idade: 90}     // Dicionario(Object)


console.log(nome)                                   // out: Bonaretti
console.log(typeof nome)                            // out: string

console.log(idade)                                  // out: 90
console.log(typeof idade)                           // out: number

console.log(logado)                                 // out: true
console.log(typeof logado)                          // out: boolean

console.log(lista)                                  // out: ['ovo', 'massa', 'corante']
console.log(typeof lista)                           // out: object

console.log(nomeCompleto)                           // out: {nome: 'Gabrile', idade: 90}
console.log(typeof nomeCompleto)                    // out: object